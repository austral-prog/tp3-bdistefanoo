def check_vowels():
    Nombre = input("Ingrese su nombre: ")
    nombre = Nombre.lower()
    print("Contiene a:", 'a' in nombre)
    print("Contiene e:", 'e' in nombre)
    print("Contiene i:", 'i' in nombre)
    print("Contiene o:", 'o' in nombre)
    print("Contiene u:", 'u' in nombre)

check_vowels()
